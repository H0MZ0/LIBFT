#include <stdio.h>

size_t	ft_strlen(const char *s)
{
	size_t	x;

	x = 0;
	while (s[x])
		x++;
	return (x);
}

size_t	ft_strlcat(char *dst, const char *src, size_t dstsize)
{
	size_t	dst_len;
	size_t	src_len;
	size_t	i;

	dst_len = ft_strlen(dst);
	src_len = ft_strlen(src);
	i = 0;
	if (dstsize > dst_len + src_len + 1)
	{
		while (src[i])
		{
			dst[dst_len] = src[i];
			i++;
			dst_len++;
		}
		return (dst_len);
	}
}
