#include "libft.h"
#include <stdio.h>

void	ft_putendl_fd(char *s, int fd)
{
	size_t	i;

	i = 0;
	while (s[i])
	{
		write (fd, s[i], 1);
		i++;
	}
	write (1, '\n', 1);
}
